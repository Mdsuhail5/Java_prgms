
public class array14 {

    public static void main(String args[]) {
        int no[] = new int[4];
        no[0] = 4;
        no[1] = 8;
        no[2] = 16;
        no[3] = 20;

        //Enhanced for loop
        for (int n : no) {
            System.out.println(n);
        }
    }
}
